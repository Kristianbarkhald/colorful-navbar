$(document).ready(function(){
    $("#btn1").hover(function(){
      $("#btn1").css("color", "white"),$(".fa-handshake-o").css("color","#e3342f");
      $("#btn2").css("color", "#3490dc"),$(".fa-clone").css("color","#3490dc");
      $("#btn3").css("color", "#ffed4a"),$(".fa-battery-half").css("color","#ffed4a");
      $("#btn4").css("color", "#38c172"),$(".fa-tablet").css("color","#38c172");
      $("#btn5").css("color", "#f66d9b"),$(".fa-address-card-o").css("color","#f66d9b");
    });
    $("#btn1").mouseleave(function(){
      $("#btn1").css("color", "white"),$(".fa-handshake-o").css("color","white");
      $("#btn2").css("color", "white"),$(".fa-clone").css("color","white");
      $("#btn3").css("color", "white"),$(".fa-battery-half").css("color","white");
      $("#btn4").css("color", "white"),$(".fa-tablet").css("color","white");
      $("#btn5").css("color", "white"),$(".fa-address-card-o").css("color","white");

    });
  });

